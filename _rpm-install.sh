#!/bin/bash
# =============================================================================
# –°–Ї—А–Є–њ—В —Г—Б—В–∞–љ–Њ–≤–Ї–Є RPM-–њ–∞–Ї–µ—В–∞ mh-compressor-manager
# –Ш—Б–њ–Њ–ї—М–Ј–Њ–≤–∞–љ–Є–µ: sudo ./_rpm-install.sh [version]
# =============================================================================

set -e

# –¶–≤–µ—В–∞ –і–ї—П –≤—Л–≤–Њ–і–∞
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

PROJECT_NAME="mh-compressor-manager"

# –Я—А–Њ–≤–µ—А–Ї–∞ root
if [ "$(id -u)" -ne 0 ]; then
    echo -e "${RED}вЬЧ –Ю—И–Є–±–Ї–∞: –≠—В–Њ—В —Б–Ї—А–Є–њ—В —В—А–µ–±—Г–µ—В –њ—А–∞–≤ root (sudo)${NC}"
    echo "–Ш—Б–њ–Њ–ї—М–Ј–Њ–≤–∞–љ–Є–µ: sudo $0 [version]"
    exit 1
fi

# –Я–Њ–ї—Г—З–µ–љ–Є–µ –≤–µ—А—Б–Є–Є
VERSION=${1:-""}
if [ -z "$VERSION" ]; then
    # –Я–Њ–њ—Л—В–Ї–∞ –љ–∞–є—В–Є –њ–Њ—Б–ї–µ–і–љ—О—О —Б–Њ–±—А–∞–љ–љ—Г—О –≤–µ—А—Б–Є—О
    RPM_BUILD_ROOT=$(rpm --eval '%{_topdir}' 2>/dev/null || echo "$HOME/rpmbuild")
    LATEST_RPM=$(find "${RPM_BUILD_ROOT}/RPMS" -name "${PROJECT_NAME}-*.rpm" -type f 2>/dev/null | sort -V | tail -1)
    if [ -n "$LATEST_RPM" ]; then
        RPM_FILE="$LATEST_RPM"
    else
        echo -e "${YELLOW}–Т–µ—А—Б–Є—П –љ–µ —Г–Ї–∞–Ј–∞–љ–∞ –Є —Б–Њ–±—А–∞–љ–љ—Л–є RPM –љ–µ –љ–∞–є–і–µ–љ.${NC}"
        echo "–Ш—Б–њ–Њ–ї—М–Ј–Њ–≤–∞–љ–Є–µ: $0 [version]"
        exit 1
    fi
else
    # –Я–Њ–Є—Б–Ї RPM –њ–Њ –≤–µ—А—Б–Є–Є
    RPM_BUILD_ROOT=$(rpm --eval '%{_topdir}' 2>/dev/null || echo "$HOME/rpmbuild")
    RPM_FILE=$(find "${RPM_BUILD_ROOT}/RPMS" -name "${PROJECT_NAME}-${VERSION}-*.rpm" -type f 2>/dev/null | head -1)
    if [ -z "$RPM_FILE" ]; then
        echo -e "${RED}вЬЧ RPM-–њ–∞–Ї–µ—В –≤–µ—А—Б–Є–Є ${VERSION} –љ–µ –љ–∞–є–і–µ–љ –≤ ${RPM_BUILD_ROOT}/RPMS/${NC}"
        exit 1
    fi
fi

echo -e "${GREEN}============================================${NC}"
echo -e "${GREEN}  –£—Б—В–∞–љ–Њ–≤–Ї–∞ RPM: ${PROJECT_NAME}${NC}"
echo -e "${GREEN}  –Я–∞–Ї–µ—В: ${RPM_FILE}${NC}"
echo -e "${GREEN}============================================${NC}"
echo ""

# –Я—А–Њ–≤–µ—А–Ї–∞ –љ–∞–ї–Є—З–Є—П —Д–∞–є–ї–∞
if [ ! -f "$RPM_FILE" ]; then
    echo -e "${RED}вЬЧ –§–∞–є–ї –љ–µ –љ–∞–є–і–µ–љ: ${RPM_FILE}${NC}"
    exit 1
fi

# =============================================================================
# [1/3] –£—Б—В–∞–љ–Њ–≤–Ї–∞/–Њ–±–љ–Њ–≤–ї–µ–љ–Є–µ –њ–∞–Ї–µ—В–∞
# =============================================================================
echo -e "${YELLOW}[1/3] –£—Б—В–∞–љ–Њ–≤–Ї–∞ RPM-–њ–∞–Ї–µ—В–∞...${NC}"

if rpm -q "${PROJECT_NAME}" &>/dev/null; then
    echo -e "${YELLOW}–Я–∞–Ї–µ—В —Г–ґ–µ —Г—Б—В–∞–љ–Њ–≤–ї–µ–љ, –≤—Л–њ–Њ–ї–љ—П–µ—В—Б—П –Њ–±–љ–Њ–≤–ї–µ–љ–Є–µ...${NC}"
    dnf upgrade -y --disablerepo="*" "$RPM_FILE"
else
    dnf install -y --disablerepo="*" "$RPM_FILE"
fi

echo -e "${GREEN}вЬУ –Я–∞–Ї–µ—В —Г—Б—В–∞–љ–Њ–≤–ї–µ–љ${NC}"
echo ""

# =============================================================================
# [2/3] –Р–Ї—В–Є–≤–∞—Ж–Є—П —Б–ї—Г–ґ–±—Л
# =============================================================================
echo -e "${YELLOW}[2/3] –Р–Ї—В–Є–≤–∞—Ж–Є—П —Б–ї—Г–ґ–±—Л...${NC}"

systemctl daemon-reload
systemctl enable "${PROJECT_NAME}"

echo -e "${GREEN}вЬУ –°–ї—Г–ґ–±–∞ –∞–Ї—В–Є–≤–Є—А–Њ–≤–∞–љ–∞${NC}"
echo ""

# =============================================================================
# [3/3] –Ч–∞–њ—Г—Б–Ї —Б–ї—Г–ґ–±—Л
# =============================================================================
echo -e "${YELLOW}[3/3] –Ч–∞–њ—Г—Б–Ї —Б–ї—Г–ґ–±—Л...${NC}"

systemctl start "${PROJECT_NAME}"
sleep 1

# –Я—А–Њ–≤–µ—А–Ї–∞ —Б—В–∞—В—Г—Б–∞
if systemctl is-active --quiet "${PROJECT_NAME}"; then
    echo -e "${GREEN}вЬУ –°–ї—Г–ґ–±–∞ –Ј–∞–њ—Г—Й–µ–љ–∞ –Є —А–∞–±–Њ—В–∞–µ—В${NC}"
else
    echo -e "${YELLOW}вЪ† –°–ї—Г–ґ–±–∞ –Ј–∞–њ—Г—Й–µ–љ–∞, –љ–Њ —В—А–µ–±—Г–µ—В –њ—А–Њ–≤–µ—А–Ї–Є${NC}"
fi

# =============================================================================
# –†–µ–Ј—Г–ї—М—В–∞—В
# =============================================================================
echo ""
echo -e "${GREEN}============================================${NC}"
echo -e "${GREEN}  вЬУ –£—Б—В–∞–љ–Њ–≤–Ї–∞ –Ј–∞–≤–µ—А—И–µ–љ–∞ —Г—Б–њ–µ—И–љ–Њ!${NC}"
echo -e "${GREEN}============================================${NC}"
echo ""
echo -e "${YELLOW}–Я—А–Њ–≤–µ—А–Ї–∞:${NC}"
echo -e "  systemctl status ${PROJECT_NAME}"
echo -e "  journalctl -u ${PROJECT_NAME} -f"
echo ""
echo -e "${YELLOW}–£–њ—А–∞–≤–ї–µ–љ–Є–µ:${NC}"
echo -e "  sudo systemctl {start|stop|restart|status} ${PROJECT_NAME}"
echo ""
